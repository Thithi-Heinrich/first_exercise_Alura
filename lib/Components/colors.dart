import 'package:flutter/material.dart';

Color colorChanging(int value){
  switch (value){
    case == 1:
      return Colors.purple;
    case == 2:
      return  Colors.pink;
    case == 3:
      return Colors.red;
    case == 4:
      return Colors.orange;
    case == 5:
      return Colors.yellow;
    case == 6:
      return Colors.black;
    default:
      return Colors.blue;
  }
}